//! # Federico Storage (A Storage Solution For Federated Applications)
//! 
//! Federico Storage works by using the BLAKE3 hash of the file and can be used with Immutable Storage Solution for permanent storage.
//! 
//! It is meant to server as censorship-resistant and easy to setup.
//! 
//! It also allows for Authentication of data.
//! 
//! It is the federated universe solution.
